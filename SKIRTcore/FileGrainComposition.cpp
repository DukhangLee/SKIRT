#include "FileGrainComposition.hpp"

//////////////////////////////////////////////////////////////////////

FileGrainComposition::FileGrainComposition()
    : _bulkmassdensity(0)

{
}

//////////////////////////////////////////////////////////////////////

void FileGrainComposition::setupSelfBefore()
{
    setBulkDensity(_bulkmassdensity);
    loadOpticalGrid(false, _opticalFilename, false, false, false, false);
    loadEnthalpyGrid(false, _calorimetricFilename);
}

//////////////////////////////////////////////////////////////////////

QString FileGrainComposition::name() const
{
    return "File_" + _opticalFilename + "_" + _calorimetricFilename;
}

//////////////////////////////////////////////////////////////////////

void FileGrainComposition::setOpticalFilename(QString value)
{
    _opticalFilename = value;
}

//////////////////////////////////////////////////////////////////////

QString FileGrainComposition::opticalFilename() const
{
    return _opticalFilename;
}

//////////////////////////////////////////////////////////////////////

void FileGrainComposition::setCalorimetricFilename(QString value)
{
    _calorimetricFilename = value;
}

//////////////////////////////////////////////////////////////////////

QString FileGrainComposition::calorimetricFilename() const
{
    return _calorimetricFilename;
}

//////////////////////////////////////////////////////////////////////

void FileGrainComposition::setBulkMassDensity(double value)
{
    _bulkmassdensity = value;
}

//////////////////////////////////////////////////////////////////////

double FileGrainComposition::bulkMassDensity() const
{
    return _bulkmassdensity;
}

