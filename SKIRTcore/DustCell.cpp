#include "DustCell.hpp"

using namespace std;

////////////////////////////////////////////////////////////////////

DustCell::DustCell(int Ncomp)
    : _volume(0), _rhov(Ncomp)
{
}

////////////////////////////////////////////////////////////////////

DustCell::~DustCell()
{
}

//////////////////////////////////////////////////////////////////////

double DustCell::volume() const
{
    return _volume;
}

//////////////////////////////////////////////////////////////////////

void DustCell::setvolume(double volume)
{
    _volume = volume;
}

//////////////////////////////////////////////////////////////////////

double DustCell::density(int i) const
{
    return _rhov[i];
}

//////////////////////////////////////////////////////////////////////

double DustCell::density() const
{
    return _rhov.sum();
}

//////////////////////////////////////////////////////////////////////

void DustCell::setdensity(int i, double rho)
{
    _rhov[i] = rho;
}

//////////////////////////////////////////////////////////////////////

double DustCell::Labs(int ell) const
{
    return Labsstellar(ell) + Labsdust(ell);
}

