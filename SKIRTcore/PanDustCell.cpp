#include "PanDustCell.hpp"
#include "WavelengthGrid.hpp"

using namespace std;

////////////////////////////////////////////////////////////////////

PanDustCell::PanDustCell(int Ncomp, int Nlambda)
    : DustCell(Ncomp), _Labsstellarv(Nlambda), _Labsdustv(Nlambda)
{
}

////////////////////////////////////////////////////////////////////

double PanDustCell::Labsstellar(int ell) const
{
    return _Labsstellarv[ell];
}

////////////////////////////////////////////////////////////////////

double PanDustCell::Labsdust(int ell) const
{
    return _Labsdustv[ell];
}

////////////////////////////////////////////////////////////////////

void PanDustCell::rebootLabsdust()
{
    _Labsdustv = 0.0;
}

////////////////////////////////////////////////////////////////////

void PanDustCell::absorb(int ell, double DeltaL, bool ynstellar)
{
    if (ynstellar)
        _Labsstellarv[ell] += DeltaL;
    else
        _Labsdustv[ell] += DeltaL;
}

